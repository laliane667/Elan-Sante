                for(let i = 0; i < columns.length; i++){
                let column = columns[i];
                var randomMarginTop = 0;
                //var randomMarginTop = Math.floor(Math.random() * 301); 
                switch(i){
                    case 0 : randomMarginTop = 10; break;
                    case 1 : randomMarginTop = 75; break;
                    case 2 : randomMarginTop = -50; break;
                    case 3 : randomMarginTop = -50; break;
                    case 4 : randomMarginTop = 10; break;
                    case 5 : randomMarginTop = 50; break;
                    case 6 : randomMarginTop = -75; break;
                    case 7 : randomMarginTop = 10; break;
                    case 8 : randomMarginTop = 50; break;
                }
                column.style.marginTop = randomMarginTop + "px";
            }
            }
            

            function getRandomPastelColor() {
                var red = Math.floor(Math.random() * 256 / 2 + 128);
                var green = Math.floor(Math.random() * 256 / 4 + 128); // Lower green component
                var blue = Math.floor(Math.random() * 256 / 4 + 128);  // Lower blue component

                // Avoiding orange by reducing the green when red is dominant
                if (red > 180) {
                    green = Math.floor(green / 2);
                }

                return `rgb(${red}, ${green}, ${blue})`;
            }

            // Select all elements with the specified class
            var elements = document.querySelectorAll('.fa-solid.fa-circle-user');

            // Set a random pastel background color for each element
            elements.forEach((element) => {
                element.style.color = getRandomPastelColor();
            });
        });

    </script>

